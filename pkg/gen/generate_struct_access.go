func getType(expr ast.Expr) (string, string, string) {
	switch t := expr.(type) {
	case *ast.Ident:
		/* 	这里不能再继续递归解析(虽然递归可以解析类型的最基础类型)。
			但是一旦代码中设立了新类型，就会出问题。
		 	比如 type X map[int32]int32
		 	X 和map[int32]int32并不等价，会出现语法错误
			(除非是 type X = map[int32]int32 这才是完全等价的类型)
		*/
		// if t.Obj != nil {
		// 	typeSpec, ok := t.Obj.Decl.(*ast.TypeSpec)
		// 	if ok {
		// 		return getType(typeSpec.Type)
		// 	}
		// 	return t.Obj.Name, "", ""
		// }
		return t.Name, "", ""
	case *ast.StructType:
		if name, ok := structNameMap[t]; ok {
			return name, "", ""
		} else {
			// 否则可能是默认的结构体, 返回默认的结构体名
			return "struct{}", "", ""
		}
	case *ast.StarExpr:
		typ, _, _ := getType(t.X)
		return "*" + typ, "", ""
	case *ast.ArrayType:
		// Len != nil 是数组
		if t.Len != nil {
			// TODO (yang.yuan) 解析数组结构(目前模板中屏蔽了数组)
			// 如果Len里面对应的不是一个基本量，而是一个表达式(比如二元表达式*ast.BinaryExpr)
			// 那么是不能正确解析长度的
			// 例子：[1<<3]int
			keyType, _, _ := getType(t.Len)
			elemType, _, _ := getType(t.Elt)
			return fmt.Sprintf("[%s]%s", keyType, elemType), "", elemType
		} else {
			// Len == nil 则是切片类型
			// 切片类型（没有固定长度）
			typ, _, _ := getType(t.Elt)
			return "[]" + typ, "", typ
		}
	case *ast.MapType:
		keyTyp, _, _ := getType(t.Key)
		valTyp, _, _ := getType(t.Value)
		return "map[" + keyTyp + "]" + valTyp, keyTyp, valTyp
	case *ast.SelectorExpr:
		xTyp, _, _ := getType(t.X)
		selTyp, _, _ := getType(t.Sel)
		return fmt.Sprintf("%s.%s", xTyp, selTyp), "", ""
	default:
		log.Println("getType unknown:", t)
		return fmt.Sprintf("%T", t), "", ""
	}
}

// 确定字段种类
func determineKind(expr ast.Expr) (kind, keyType, valueType string) {
	switch t := expr.(type) {
	case *ast.Ident:
		// if t.Obj != nil {
		// 	typeSpec, ok := t.Obj.Decl.(*ast.TypeSpec)
		// 	if ok {
		// 		return determineKind(typeSpec.Type)
		// 	}
		// 	return t.Obj.Name, "", ""
		// }
		return t.Name, "", ""
	case *ast.StarExpr:
		typ, _, _ := determineKind(t.X)
		// 进行拼接(指针类型的数据)
		return "*" + typ, "", ""
	case *ast.ArrayType:
		elemType, _, _ := determineKind(t.Elt)
		if t.Len == nil {
			return "slice", "", elemType
		} else {
			return "array", "", elemType
		}
	case *ast.MapType:
		keyType, _, _ = determineKind(t.Key)
		valueType, _, _ = determineKind(t.Value)
		return "map", keyType, valueType
	case *ast.SelectorExpr:
		kind, _, _ = determineKind(t.X)
		valueType, _, _ = determineKind(t.Sel)
		return fmt.Sprintf("%s.%s", kind, valueType), "", ""
	case *ast.StructType:
		return "struct", "", ""
	case *ast.FuncType:
		return "func", "", ""
	case *ast.InterfaceType:
		return "interface", "", ""
	case *ast.IndexExpr:
		return "index", "", ""
	case *ast.ChanType:
		return "chan", "", ""
	default:
		log.Println("determineKind unknown", t)
		return "unknown", "", ""
	}
}

var structNameMap = map[*ast.StructType]string{}

// 获取一个Go文件中的所有结构体
func parseStructsFromFile(filename string) ([]*TemplateData, string, error) {
	src, err := os.ReadFile(filename)
	if err != nil {
		return nil, "", err
	}

	// 解析 Go 代码
	fset := token.NewFileSet()
	node, err := parser.ParseFile(fset, filename, src, parser.AllErrors)
	if err != nil {
		return nil, "", err
	}

	// 存储结构体信息
	var structs []*TemplateData
	packageName := node.Name.Name
	dateTime := time.Now().Format(time.DateTime)
	dir := filepath.Dir(filename)

	// 遍历 AST
	for _, decl := range node.Decls {
		genDecl, ok := decl.(*ast.GenDecl)
		if !ok || genDecl.Tok != token.TYPE {
			continue
		}
		for _, spec := range genDecl.Specs {
			typeSpec, ok := spec.(*ast.TypeSpec)
			if !ok {
				continue
			}
			structType, ok := typeSpec.Type.(*ast.StructType)
			if !ok {
				continue
			}

			// 统计结构体的名字
			structNameMap[structType] = typeSpec.Name.Name
		}
	}

	// 遍历 AST
	for _, decl := range node.Decls {
		genDecl, ok := decl.(*ast.GenDecl)
		if !ok || genDecl.Tok != token.TYPE {
			continue
		}
		for _, spec := range genDecl.Specs {
			typeSpec, ok := spec.(*ast.TypeSpec)
			if !ok {
				continue
			}
			structType, ok := typeSpec.Type.(*ast.StructType)
			if !ok {
				continue
			}

			// 解析字段信息
			var fields []*Field
			for _, field := range structType.Fields.List {
				// 解析原生(基本)类型
				kind, keyKind, valueKind := determineKind(field.Type)
				// 解析具体类型
				// 如果完全解析成具体类型会导致一个问题：有些类型是新类型
				// 虽然本质上(对应的数据)是一样的，但是不能通过Go语言的语法检查
				// 其实有细微的差别——新类型可以新增方法等(所以认为是全新的类型)
				fieldType, keyType, valueType := getType(field.Type)

				for _, fieldName := range field.Names {
					fields = append(fields, &Field{
						Name:      fieldName.Name,
						Kind:      kind,
						KeyKind:   keyKind,
						ValueKind: valueKind,
						Type:      fieldType,
						KeyType:   keyType,
						ValueType: valueType,
					})
				}
			}

			// 存储结构体信息
			structs = append(structs, &TemplateData{
				Dir:         dir,
				PackageName: packageName,
				DateTime:    dateTime,
				StructName:  typeSpec.Name.Name,
				Fields:      fields,
			})
		}
	}
	// 重置
	structNameMap = map[*ast.StructType]string{}
	return structs, packageName, nil
}

// 遍历指定路径下的所有go文件
func findGoFiles(root string) ([]string, error) {
	var files []string
	// 定义排除的目录模式
	//excludeVendorPattern := regexp.MustCompile(`(?i)/vendor/`)
	err := filepath.Walk(root, func(path string, info os.FileInfo, err error) error {
		if err != nil {
			return err
		}
		// 跳过vendor目录
		// if excludeVendorPattern.MatchString(path) {
		// 	return filepath.SkipDir
		// }
		if strings.Contains(path, "vendor") {
			return filepath.SkipDir
		}
		// 只处理Go文件并且不是测试文件
		if !info.IsDir() && strings.HasSuffix(info.Name(), ".go") &&
			!strings.HasSuffix(info.Name(), "_test.go") {
			files = append(files, path)
		}
		return nil
	})
	return files, err
}

func trimTypeStr(typStr, packageName string) string {
	if strings.HasPrefix(typStr, "*") {
		// 指针类型
		prefix := "*" + packageName + "."
		if strings.HasPrefix(typStr, prefix) {
			// 在同一包下的数据需要去掉包名
			return "*" + strings.TrimPrefix(typStr, prefix)
		} else {
			return typStr
		}
	} else {
		// 普通类型
		prefix := packageName + "."
		if strings.HasPrefix(typStr, prefix) {
			return strings.TrimPrefix(typStr, prefix)
		} else {
			return typStr
		}
	}
}

func generateOneStruct(tmpl *template.Template, templateData *TemplateData) {
	outputFileName := filepath.Join(templateData.Dir, strings.ToLower(templateData.StructName)+"_access.go")
	file, err := os.Create(outputFileName)
	if err != nil {
		log.Fatal("generateOneStruct 创建文件时出错:", err, "\t文件名:", outputFileName)
	}
	defer func() {
		err = file.Close()
		if err != nil {
			log.Fatal("generateOneStruct 关闭文件时出错:", err, "\t文件名:", outputFileName)
		}
	}()

	err = tmpl.Execute(file, templateData)
	if err != nil {
		log.Fatal("generateOneStruct 执行模版时出错:", err, "\t文件名:", outputFileName)
	}

	// 执行 goimports 命令进行格式化
	cmd := exec.Command("goimports", "-w", outputFileName)
	cmd.Stdout = os.Stdout
	cmd.Stderr = os.Stderr

	err = cmd.Run()
	if err != nil {
		log.Fatal("generateOneStruct 格式化生成代码时出错:", err, "\t文件名:", outputFileName)
	}

	// absPath, err := filepath.Abs(outputFileName)
	// if err != nil {
	// 	log.Fatal("获取绝对路径失败:", err)
	// }
	log.Println("✅ 代码已生成:", outputFileName, "\t结构体名:", templateData.StructName)
}

func generateCode(tmpl *template.Template, structs []*TemplateData) {
	for _, data := range structs {
		generateOneStruct(tmpl, data)
	}
}

func GenerateCodes(fileStr string) {
	/*
		获取文件
	*/
	// 遍历指定文件或目录，获取所有Go文件
	files, err := findGoFiles(fileStr)
	if err != nil {
		log.Fatal(err)
	}

	/*
		模板解析
	*/
	// 注册函数，方便模板使用
	funcMap := template.FuncMap{
		"hasPrefix":   strings.HasPrefix,
		"trimPrefix":  strings.TrimPrefix,
		"trimTypeStr": trimTypeStr,
		"trimPointer": func(s string) string {
			return strings.TrimPrefix(s, "*")
		},
	}
	// 解析模板
	tmpl, err := template.New("codegen").Funcs(funcMap).Parse(getterTemplate1)
	if err != nil {
		log.Fatal(err, "解析模板失败", "\t模板名:", "codegen", "\t目录或文件:", fileStr)
	}

	/*
		生成代码
	*/
	for i, file := range files {
		//log.Println("🚀 开始解析第", i+1,"个文件:", file)
		structs, _, err := parseStructsFromFile(file)
		if err != nil {
			log.Fatalln("generateCodes解析失败:", err, "\t文件名:", file)
			continue
		}
		//log.Println("🎉 解析成功:", file, "结构体数量:", len(structs))
		if len(structs) == 0 {
			//log.Println("❌ 未找到结构体，跳过生成代码:", file, "\n")
			continue
		}
		log.Println("🚀 开始生成代码:", file, "结构体数量:", len(structs), "第", i+1, "个文件")
		// 生成代码
		generateCode(tmpl, structs)
		log.Println("🎉 代码生成成功:", file, "\n")
	}
	log.Println("扫描目录:", fileStr)
	log.Println("扫描", len(files), "个文件，生成代码成功！")
	log.Println("🎉🎉🎉 代码生成完成！🎉🎉🎉")
	log.Println("🎉🎉🎉 Enjoy it!😁 🎉🎉🎉")
}