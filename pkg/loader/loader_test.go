// TestLoad_NoGoFilesSkipped 验证 "no Go files" 错误在 phase 1 和 phase 2 均被静默跳过。
//
// 场景：
//   - pkg/  含损坏的 _access.go → phase 1 触发 overlay → 进入 phase 2
//   - empty/ 是空目录，go/packages 对其报 "no Go files" 错误
//
// 期望：两个阶段都跳过该错误（isNoGoFilesError == true），Load 最终成功。
func TestLoad_NoGoFilesSkipped(t *testing.T) {
	dir := makeTempModule(t, map[string]string{
		"pkg/user.go": `package pkg
type User struct { Name string }
`,
		// 含 gogen 标记的损坏文件 → phase 1 检测到 → 加入 overlay → 触发 phase 2
		"pkg/user_access.go": `// Code generated by gogen; DO NOT EDIT.
package pkg
BAD SYNTAX
`,
	})
	// 创建空子目录（无 .go 文件），go/packages 对其报 "no Go files"
	require.NoError(t, os.MkdirAll(filepath.Join(dir, "empty"), 0o755))

	// phase 1：pkg/ 的 _access.go 错误 → overlay 非空 → 进入 phase 2
	//          empty/ 的 "no Go files" 错误 → isNoGoFilesError → 静默跳过
	// phase 2：重新加载后 empty/ 仍有 "no Go files" → isNoGoFilesError → 静默跳过
	//          pkg/ 在 overlaidDirs → 跳过
	// 最终 remainErrs 为空 → Load 成功
	pkgs, err := loader.Load(dir, loader.Config{}, "./pkg", "./empty")
	require.NoError(t, err, "'no Go files' 错误应被静默跳过，Load 不应报错")
	require.NotEmpty(t, pkgs, "期望加载到至少一个包")
}

// TestExtractFileFilter 验证从 patterns 中提取显式 .go 文件路径
func TestExtractFileFilter(t *testing.T) {
	dir := "/some/project"

	cases := []struct {
		name     string
		patterns []string
		want     []string
	}{
		{
			name:     "无显式文件时返回空",
			patterns: []string{"./..."},
			want:     nil,
		},
		{
			name:     "相对路径 .go 文件转绝对路径",
			patterns: []string{"./foo.go"},
			want:     []string{filepath.Join(dir, "./foo.go")},
		},
		{
			name:     "绝对路径 .go 文件原样保留",
			patterns: []string{"/abs/path/bar.go"},
			want:     []string{"/abs/path/bar.go"},
		},
		{
			name:     "file= 前缀格式，相对路径",
			patterns: []string{"file=baz.go"},
			want:     []string{filepath.Join(dir, "baz.go")},
		},
		{
			name:     "file= 前缀格式，绝对路径",
			patterns: []string{"file=/abs/baz.go"},
			want:     []string{"/abs/baz.go"},
		},
		{
			name:     "混合：.go 文件 + 包路径 + file= 格式",
			patterns: []string{"./foo.go", "./...", "file=bar.go"},
			want: []string{
				filepath.Join(dir, "./foo.go"),
				filepath.Join(dir, "bar.go"),
			},
		},
		{
			name:     "空 patterns",
			patterns: []string{},
			want:     nil,
		},
	}

	for _, tc := range cases {
		t.Run(tc.name, func(t *testing.T) {
			got := loader.ExtractFileFilter(dir, tc.patterns)
			require.Len(t, got, len(tc.want), "got %v, want %v", got, tc.want)
			for i := range got {
				assert.Equal(t, tc.want[i], got[i])
			}
		})
	}
}
