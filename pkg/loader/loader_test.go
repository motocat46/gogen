// TestLoad_Phase2_OverlaidDirCascadeErrors 验证 phase 2 中 overlaidDirs 包的级联错误被跳过。
//
// 场景：同一个包里既有损坏的 *_access.go（触发 overlay），又有调用生成方法的消费者文件。
// Phase 2 overlay 后消费者文件产生 "undefined" 级联错误，但该包目录在 overlaidDirs 中，
// 应被静默跳过，Load 应成功返回。
func TestLoad_Phase2_OverlaidDirCascadeErrors(t *testing.T) {
	dir := makeTempModule(t, map[string]string{
		"pkg/user.go": `package pkg
type User struct { Name string }
`,
		// 含 gogen 标记的损坏文件（phase 1 出错 → 触发 overlay）
		// 同时定义了 GetName 方法，overlay 后该方法消失
		"pkg/user_access.go": `// Code generated by gogen; DO NOT EDIT.
package pkg
func (u *User) GetName() string { return u.Name }
INVALID SYNTAX HERE
`,
		// 调用 GetName，overlay 后产生级联 "undefined" 错误
		"pkg/consumer.go": `package pkg
func Greet(u *User) string { return u.GetName() }
`,
		// 辅助包（合法），确保 ./... 加载多个包，覆盖非 overlaid 包的 for 循环路径
		"helper/helper.go": `package helper
func Noop() {}
`,
	})

	// phase1: user_access.go 损坏 → overlay；consumer.go 级联错误 → otherErrs（但 overlay 非空 → 继续）
	// phase2: user_access.go 被 overlay，pkg/ 目录在 overlaidDirs → 级联错误被跳过
	// helper/ 不在 overlaidDirs → 进入 for 循环，但无错误
	pkgs, err := loader.Load(filepath.Join(dir, "pkg"), loader.Config{}, ".")
	require.NoError(t, err, "phase2 中 overlaidDirs 包的级联错误应被跳过")
	require.NotEmpty(t, pkgs, "期望加载到至少一个包")
}

// TestLoad_Phase2_RemainErrors 验证 phase 2 中非 overlaid 包的错误导致 Load 返回 error。
//
// 场景：pkg/ 有损坏的 _access.go（触发 overlay），另有独立的 broken/ 包含用户代码错误。
// phase 2 中 broken/ 不在 overlaidDirs → 其错误进入 remainErrs → Load 报错。
func TestLoad_Phase2_RemainErrors(t *testing.T) {
	dir := makeTempModule(t, map[string]string{
		"pkg/user.go": `package pkg
type User struct { Name string }
`,
		// 损坏的生成文件 → 触发 overlay
		"pkg/user_access.go": `// Code generated by gogen; DO NOT EDIT.
package pkg
BAD SYNTAX
`,
		// 独立包，含用户代码错误，与 overlay 无关
		"broken/bad.go": `package broken
var _ = undefinedSymbol
`,
	})

	// phase1: user_access.go 错误 → overlay；broken/ 错误 → otherErrs
	// 因 overlay 非空，进入 phase 2
	// phase2: broken/ 不在 overlaidDirs → remainErrs 非空 → 返回 error
	_, err := loader.Load(dir, loader.Config{}, "./...")
	require.Error(t, err, "phase2 中非 overlaid 包的错误应导致 Load 返回 error")
}

